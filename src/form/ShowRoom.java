    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            // TODO add your handling code here:
          
            txtTip.setEnabled(false);
            txtBrKreveta.setEnabled(false);
            comboDostupna.setEnabled(false);
            txtCena.setEnabled(false);
            txtPogled.setEnabled(false);
            
            Soba soba = new Soba();
            soba.setId(Long.parseLong(txtID.getText().trim()));
            soba.setTip(txtTip.getText().trim());
            String broj = txtBrKreveta.getText().trim();
            try {
            int value = Integer.parseInt(broj);
            soba.setBrojKreveta(value);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please enter a valid numeric value for br. kreveta.");
            return;
        }
            soba.setPogled(txtPogled.getText().trim());
            String cena = txtCena.getText().trim();
            try {
            double value = Double.parseDouble(cena);  
            soba.setCena(value);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please enter a valid numeric value for cena.");
            return;
        }
            soba.setDostupna(Boolean.parseBoolean(comboDostupna.getSelectedItem().toString()));
            Zahtev zahtev = new Zahtev(Operacija.U_SOBA, soba);
            Kontroler.getInstance().createObject(zahtev);
        } catch (IOException ex) {
            Logger.getLogger(ShowRoom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

